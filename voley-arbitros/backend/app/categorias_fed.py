def cargar():
    """Devuelve [{'id','nombre','anotador','min_arbitros','primer_arbitro',
    'segundo_arbitro','prioridad'}] de cada categoría."""
    salida = []
    for linea in _RAW.strip().splitlines():
        campos = re.split(r"\t+| {2,}", linea.strip())
        if len(campos) < 7:
            continue
        idc, nombre, anotador, minarb, primero, segundo, prioridad = campos[:7]
        salida.append(
            {
                "id": idc.strip(),
                "nombre": " ".join(nombre.split()),
                "anotador": _nivel(anotador),
                "min_arbitros": _int(minarb, 1),
                "primer_arbitro": _nivel(primero),
                "segundo_arbitro": _nivel(segundo),
                "prioridad": _int(prioridad, None),
            }
        )
    return salida


def _nivel(valor):
    v = (valor or "").strip()
    return None if v.upper() in ("NULL", "NO", "") else v


def _int(valor, defecto):
    try:
        return int(str(valor).strip())
    except (TypeError, ValueError):
        return defecto
