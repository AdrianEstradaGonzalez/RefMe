def cargar():
    """Devuelve [{'id', 'nombre', 'latitud', 'longitud'}] desde el bloque crudo."""
    salida = []
    for linea in _RAW.strip().splitlines():
        partes = linea.split("\t") if "\t" in linea else linea.split()
        if len(partes) < 4:
            # Reconstruir por los dos últimos tokens (coordenadas).
            tokens = linea.split()
            if len(tokens) < 4:
                continue
            partes = [tokens[0], " ".join(tokens[1:-2]), tokens[-2], tokens[-1]]
        pid, nombre, lat, lon = partes[0], partes[1], partes[-2], partes[-1]
        try:
            latitud = float(lat.replace(",", "."))
            longitud = float(lon.replace(",", "."))
        except ValueError:
            continue
        salida.append(
            {
                "id": pid.strip(),
                "nombre": " ".join(nombre.split()),
                "latitud": latitud,
                "longitud": longitud,
            }
        )
    return salida
